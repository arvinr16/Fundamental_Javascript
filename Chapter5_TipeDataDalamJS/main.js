// Materi Tipe Data dalam Javascript
// 1. 'console.log(typeof 'nama variable');' = Digunakan untuk mengetahui jenis Tipe Data

let dataString = "Paijo";    // Tipe Data = String
console.log(dataString);
console.log(typeof dataString);

console.log("")

let dataNumber = 21;        // Tipe Data = Number
console.log(dataNumber);
console.log(typeof dataNumber);

console.log("")

let dataBoolean = true;     // Tipe Data = Boolean
console.log(dataBoolean);
console.log(typeof dataBoolean);

console.log("")

let dataKosong;             // Tipe Data = Undefined
console.log(dataKosong);    // Contoh kasus mengganti Tipe Data pada suatu variable
console.log(typeof dataKosong);
dataKosong = "Dalijo";
console.log(dataKosong);
console.log(typeof dataKosong);