// Operator Logika
/* Javascript memiliki 3 Operator Logika utama, yaitu:
   1. '&&'(AND) = Menghasilkan True hanya jika kedua nilai bernilai True.
   2. '||'(OR)  = Menghasilkan True jika minimal satu nilai bernilai True.
   3. '!'(NOT)  = Membalik nilai. Jika True jadi False, jika False jadi True.
*/